package base.jvm_learning;

public class Basic_Type {
    public static void main(String[] args) {
        int a = 3;
        int b = 4;
        swap(a, b);
        System.out.println(a);
        System.out.println(b);
    }

    private static void swap(int a, int b) {
        a = 4;
        b = 3;
    }
}




